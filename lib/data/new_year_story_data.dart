  /// 根据ID获取故事
  static Map<String, dynamic>? getStoryById(String id) {
    try {
      return getAllStories().firstWhere((story) => story['id'] == id);
    } catch (e) {
      return null;
    }
  }

  /// 获取随机故事
  static Map<String, dynamic> getRandomStory() {
    final stories = getAllStories();
    stories.shuffle();
    return stories.first;
  }
