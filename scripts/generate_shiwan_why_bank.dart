import 'dart:convert';
import 'dart:io';

class Seed {
  final String category;
  final String emoji;
  final String question;
  final String correct;
  final String wrong;
  final String explanation;

  const Seed(
    this.category,
    this.emoji,
    this.question,
    this.correct,
    this.wrong,
    this.explanation,
  );
}

void main() {
  final items = _buildQuestionBank();
  _validate(items);

  final counts = <String, int>{};
  final output = <Map<String, dynamic>>[];
  for (var i = 0; i < items.length; i++) {
    final seed = items[i];
    final next = (counts[seed.category] ?? 0) + 1;
    counts[seed.category] = next;
    final correctFirst = _stableEven(seed.question, i);
    final options =
        correctFirst ? [seed.correct, seed.wrong] : [seed.wrong, seed.correct];

    output.add({
      'id': 'why_${seed.category}_${next.toString().padLeft(3, '0')}',
      'question': seed.question,
      'emoji': seed.emoji,
      'options': options,
      'correctIndex': correctFirst ? 0 : 1,
      'answer': seed.correct,
      'explanation': seed.explanation,
      'category': seed.category,
      'source': 'remote',
    });
  }

  final file = File('docs/shiwan_why_questions_full.json');
  file.writeAsStringSync(
    const JsonEncoder.withIndent('  ').convert(output),
    encoding: utf8,
  );

  stdout.writeln('Generated ${output.length} questions: ${file.path}');
  for (final entry in counts.entries) {
    stdout.writeln('${entry.key}: ${entry.value}');
  }
}

bool _stableEven(String text, int index) {
  final sum = text.runes.fold<int>(index, (value, rune) => value + rune);
  return sum.isEven;
}

void _validate(List<Seed> items) {
  final questions = <String>{};
  for (final item in items) {
    if (!questions.add(item.question)) {
      throw StateError('重复题目: ${item.question}');
    }
    if (item.correct.trim().isEmpty || item.wrong.trim().isEmpty) {
      throw StateError('选项为空: ${item.question}');
    }
    if (item.correct == item.wrong) {
      throw StateError('正误选项相同: ${item.question}');
    }
    if (item.explanation.contains('生活中可以多观察类似现象') ||
        item.explanation.contains('慢慢你会更理解')) {
      throw StateError('解释过于空泛: ${item.question}');
    }
  }
}

